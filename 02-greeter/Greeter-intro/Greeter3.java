public class Greeter3 {
    /* Attributes go here
       They're called instance variables
       - think of them like Turtle variables
    */
    public String s = "Hello String!";
    

    public void greet() {
	System.out.println(s);
    }

    public void ungreet() {
	System.out.println("I'm out.");
    }

}
